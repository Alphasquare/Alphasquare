<h2>What's Alphasquare?</h2>
<p>Alphasquare is a social debating application focused on discussing the evolution of programming and the web. Follow @Alphasquare for more!</p>
<br>
	<h2>Why join?</h2>
	<p>Alphasquare is a community of fellow developers and designers discussing the evolution of the web and current technologies and programming practices. Believe us, it's really fun! Debates are posted daily and there's always something to discuss. So go ahead and join our awesome community of developers and designers!</p><br>
